package session4;

public class ifelse {

	public static void main(String[] args) {
		int x = 20;

		
	      if( x == 10 ) {
	         System.out.print("Value of X is 10");
	      }else if( x == 20 ) {
	         System.out.print("Value of X is 20");
	      }else if( x == 30 ) {
	         System.out.print("Value of X is 30");
	      }else {
	         System.out.print("This is invalid value");
	      }

	}

}

